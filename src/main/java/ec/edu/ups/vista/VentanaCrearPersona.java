    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        String cedula= txtCedula.getText();
        String nombre=txtNombre.getText();
        String apellido= txtApellido.getText();
        String genero= (String) cbxGenero.getSelectedItem();
        String fechaDeNacimiento= txtFechaDeNacimiento.getText();
        String estadoCivil= (String) cbxEstado.getSelectedItem();
        String direccion= txtDireccion.getText();
        String rol=(String) cbxRol.getSelectedItem();
        
        if(nombre.isEmpty()||apellido.isEmpty()||genero.equals("Seleccione")||fechaDeNacimiento.isEmpty()||cedula.isEmpty()|| estadoCivil.equals("Seleccione")||direccion.isEmpty()||rol.equals("Seleccione")){
            JOptionPane.showMessageDialog(this, "Â¡Llene todos los campos solicitados!");
        }else{
            Participantes par = new Participantes(estadoCivil,cedula,nombre,apellido,genero,fechaDeNacimiento,direccion,rol);
            if(controladorPartisipantes.create(par)==true){
               
                System.out.println(controladorPartisipantes.findAll());
                
                JOptionPane.showMessageDialog(this, "Persona registrada con exito");
                limpiar();
                dispose();
                try {
                    controladorPartisipantes.guardarDatos(ruta);
                } catch (IOException ex) {
                    JOptionPane.showMessageDialog(this, ex.getMessage());
                }
               
            }
        }
        
    }//GEN-LAST:event_btnAgregarActionPerformed


